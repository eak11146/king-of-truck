<h5>ใหม่! ครั้งแรกของระบบควบคุมการทรงตัวอัจฉริยะ IESC (Isuzu Electronic Stability Control)</h5>
<p>ในรุ่น FVM 240 IESC ช่วยเพิ่มเสถียรภาพ และควบคุมการทรงตัวในขณะขับขี่โดยอัตโนมัติด้วยระบบอิเล็กทรอนิกส์ โดยการจับเซ็นเซอร์จากการหมุนของล้อ องศาการหมุนของพวงมาลัย และแรงเหวี่ยงของรถ ซึ่งเกิดจากการทำงานร่วมกัน 4 ระบบ คือ</p>
